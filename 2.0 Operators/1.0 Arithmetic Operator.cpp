#include <bits/stdc++.h>
#include<iostream>
using namespace std;
    
int main(){
    int x = 7;
    int y = 2;

    cout<<x+y<<endl;
    cout<<x-y<<endl;
    cout<<x*y<<endl;
    cout<<x/y<<endl;
    cout<<x%y<<endl;
    cout<<x++<<endl;
    cout<<x--<<endl;
    cout<<++y<<endl;
    cout<<--y<<endl;
    return 0;
}