int main(){
    int  x = 5;
    cout<<"x = "<<x<<endl;

    x++;
    
    cout<<"x = "<<x<<endl;

    x++;
    cout<<"x = "<<x<<endl;

    ++x;
    
    cout<<"x = "<<x<<endl;

    ++x;
    cout<<"x = "<<x<<endl;


    --x;
    
    cout<<"x = "<<x<<endl;
    --x;
    cout<<"x = "<<x<<endl;
    x--;
    cout<<"x = "<<x<<endl;
    x--;
    cout<<"x = "<<x<<endl;


    cout<<--x<<endl;
    cout<<x--<<endl;
    cout<<x;
    return 0;
}